package firstproject;

public class LoopALLODD {
    public static void main(String[] args) {
        for (int i=100; i>=0; i-=1)
        {
            System.out.print(i);
            System.out.print(",");
        }
        System.out.println("");
        for(int i=2000;i>=1000;i-=2)
        {
            System.out.print(i);
            System.out.print(",");
        }
    }
}
